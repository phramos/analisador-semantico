public class ItemWhile {
    private String labelBack;
    private String labelAfter;

    public ItemWhile(String labelBack, String labelAfter) {
        this.labelBack = labelBack;
        this.labelAfter = labelAfter;
    }

    public String getLabelBack() {
        return labelBack;
    }

    public void setLabelBack(String labelBack) {
        this.labelBack = labelBack;
    }

    public String getLabelAfter() {
        return labelAfter;
    }

    public void setLabelAfter(String labelAfter) {
        this.labelAfter = labelAfter;
    }
}
